def get_ethical_rating(headers_data):
    """
    –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ—Ç –æ–±—â–∏–π —ç—Ç–∏—á–µ—Å–∫–∏–π —Ä–µ–π—Ç–∏–Ω–≥ –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏ –Ω–∞ –æ—Å–Ω–æ–≤–µ –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤.

    Args:
        headers_data (list): –°–ø–∏—Å–æ–∫ –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤ —Å –ø–æ–ª—è–º–∏ 'name' –∏ 'present'

    Returns:
        dict: –≠—Ç–∏—á–µ—Å–∫–∏–π —Ä–µ–π—Ç–∏–Ω–≥ —Å –ø–æ—è—Å–Ω–µ–Ω–∏—è–º–∏
    """
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤ –≤–Ω—É—Ç—Ä–∏ —Ñ—É–Ω–∫—Ü–∏–∏
    critical_headers = [
        "Content-Security-Policy",
        "Strict-Transport-Security",
        "X-Frame-Options"
    ]

    important_headers = [
        "X-Content-Type-Options",
        "Referrer-Policy",
        "Permissions-Policy"
    ]

    # –ü–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ–º –ø—Ä–∏—Å—É—Ç—Å—Ç–≤—É—é—â–∏–µ –∑–∞–≥–æ–ª–æ–≤–∫–∏
    present_critical = sum(1 for h in headers_data
                           if h['name'] in critical_headers and h['present'])
    present_important = sum(1 for h in headers_data
                            if h['name'] in important_headers and h['present'])

    total_headers = len(critical_headers) + len(important_headers)
    present_total = present_critical + present_important

    # –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ–º –æ—Ü–µ–Ω–∫—É
    score = (present_total / total_headers) * 100 if total_headers > 0 else 0

    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º —É—Ä–æ–≤–µ–Ω—å
    if score >= 80:
        rating = "üü¢ –û—Ç–ª–∏—á–Ω–æ"
        description = "–í—ã—Å–æ–∫–∏–π —É—Ä–æ–≤–µ–Ω—å —ç—Ç–∏—á–µ—Å–∫–æ–π –æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ—Å—Ç–∏"
    elif score >= 60:
        rating = "üü° –£–¥–æ–≤–ª–µ—Ç–≤–æ—Ä–∏—Ç–µ–ª—å–Ω–æ"
        description = "–°—Ä–µ–¥–Ω–∏–π —É—Ä–æ–≤–µ–Ω—å, –µ—Å—Ç—å –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç–∏ –¥–ª—è —É–ª—É—á—à–µ–Ω–∏—è"
    else:
        rating = "üî¥ –ù–µ—É–¥–æ–≤–ª–µ—Ç–≤–æ—Ä–∏—Ç–µ–ª—å–Ω–æ"
        description = "–ù–∏–∑–∫–∏–π —É—Ä–æ–≤–µ–Ω—å –∑–∞—â–∏—Ç—ã –ø—Ä–∏–≤–∞—Ç–Ω–æ—Å—Ç–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π"

    return {
        "rating": rating,
        "score": round(score),
        "description": description,
        "critical_present": present_critical,
        "critical_total": len(critical_headers),
        "important_present": present_important,
        "important_total": len(important_headers)
    }


